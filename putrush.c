void		ft_putrush(char c, int a, int b, int x)
{
	if (x > 0)
		write(1, " || ", 4);
	write(1, "[colle-0", 8);
	write(1, &c, 1);
	write(1, "] [", 3);
	write(1, &a, 1);
	write(1, "] [", 3);
	write(1, &b, 1);
	write(1, "]", 1);
}
